    public void keyCatcher(javax.swing.JTextField field,java.awt.event.KeyEvent evt){
        char key = evt.getKeyChar();
        if(!Character.isDigit(key) && key != '.'){
            evt.consume();
        }
        if (key == '.'){
            String thisText = field.getText();
            for (int i = 0; i < thisText.length(); i++) {
                if (thisText.charAt(i) == '.'){
                    evt.consume();
                    break;
                }
            }
        }        
    }
    

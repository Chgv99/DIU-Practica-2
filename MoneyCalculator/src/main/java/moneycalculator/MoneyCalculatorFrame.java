    private void euroFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_euroFieldKeyReleased
        float ratio;
        float eur;
        try {
            ratio = Float.parseFloat(ratioField.getText());
            eur = Float.parseFloat(euroField.getText());
        } catch (java.lang.NumberFormatException e) {
            return;
        }
        ratio = ((float) java.lang.Math.round(ratio * 100))/100; 
        eur = ((float) java.lang.Math.round(eur * 100))/100; 
        usdField.setText(String.valueOf(((float) java.lang.Math.round(ratio*eur*100))/100));
    }//GEN-LAST:event_euroFieldKeyReleased

